#include <stdint.h>
#include <stdio.h>
#include "skinny.h"

/**
 * SKINNY-128-384 block cipher encryption.
 * Under 48-byte tweakey at k, encrypt 16-byte plaintext at p and store the 16-byte output at c.
 */
void skinny(unsigned char *c, const unsigned char *p, const unsigned char *k)
{
     unsigned char sBox8[256] = {
       0x65, 0x4c, 0x6a, 0x42, 0x4b, 0x63, 0x43, 0x6b, 0x55, 0x75, 0x5a, 0x7a, 0x53, 0x73, 0x5b, 0x7b,
       0x35, 0x8c, 0x3a, 0x81, 0x89, 0x33, 0x80, 0x3b, 0x95, 0x25, 0x98, 0x2a, 0x90, 0x23, 0x99, 0x2b,
       0xe5, 0xcc, 0xe8, 0xc1, 0xc9, 0xe0, 0xc0, 0xe9, 0xd5, 0xf5, 0xd8, 0xf8, 0xd0, 0xf0, 0xd9, 0xf9,
       0xa5, 0x1c, 0xa8, 0x12, 0x1b, 0xa0, 0x13, 0xa9, 0x05, 0xb5, 0x0a, 0xb8, 0x03, 0xb0, 0x0b, 0xb9,
       0x32, 0x88, 0x3c, 0x85, 0x8d, 0x34, 0x84, 0x3d, 0x91, 0x22, 0x9c, 0x2c, 0x94, 0x24, 0x9d, 0x2d,
       0x62, 0x4a, 0x6c, 0x45, 0x4d, 0x64, 0x44, 0x6d, 0x52, 0x72, 0x5c, 0x7c, 0x54, 0x74, 0x5d, 0x7d,
       0xa1, 0x1a, 0xac, 0x15, 0x1d, 0xa4, 0x14, 0xad, 0x02, 0xb1, 0x0c, 0xbc, 0x04, 0xb4, 0x0d, 0xbd,
       0xe1, 0xc8, 0xec, 0xc5, 0xcd, 0xe4, 0xc4, 0xed, 0xd1, 0xf1, 0xdc, 0xfc, 0xd4, 0xf4, 0xdd, 0xfd,
       0x36, 0x8e, 0x38, 0x82, 0x8b, 0x30, 0x83, 0x39, 0x96, 0x26, 0x9a, 0x28, 0x93, 0x20, 0x9b, 0x29,
       0x66, 0x4e, 0x68, 0x41, 0x49, 0x60, 0x40, 0x69, 0x56, 0x76, 0x58, 0x78, 0x50, 0x70, 0x59, 0x79,
       0xa6, 0x1e, 0xaa, 0x11, 0x19, 0xa3, 0x10, 0xab, 0x06, 0xb6, 0x08, 0xba, 0x00, 0xb3, 0x09, 0xbb,
       0xe6, 0xce, 0xea, 0xc2, 0xcb, 0xe3, 0xc3, 0xeb, 0xd6, 0xf6, 0xda, 0xfa, 0xd3, 0xf3, 0xdb, 0xfb,
       0x31, 0x8a, 0x3e, 0x86, 0x8f, 0x37, 0x87, 0x3f, 0x92, 0x21, 0x9e, 0x2e, 0x97, 0x27, 0x9f, 0x2f,
       0x61, 0x48, 0x6e, 0x46, 0x4f, 0x67, 0x47, 0x6f, 0x51, 0x71, 0x5e, 0x7e, 0x57, 0x77, 0x5f, 0x7f,
       0xa2, 0x18, 0xae, 0x16, 0x1f, 0xa7, 0x17, 0xaf, 0x01, 0xb2, 0x0e, 0xbe, 0x07, 0xb7, 0x0f, 0xbf,
       0xe2, 0xca, 0xee, 0xc6, 0xcf, 0xe7, 0xc7, 0xef, 0xd2, 0xf2, 0xde, 0xfe, 0xd7, 0xf7, 0xdf, 0xff,
      };

   unsigned char roundConstants[62] = {
       0x01, 0x03, 0x07, 0x0F, 0x1F, 0x3E, 0x3D, 0x3B, 0x37, 0x2F, 0x1E, 0x3C, 0x39, 0x33,
       0x27, 0x0E, 0x1D, 0x3A, 0x35, 0x2B, 0x16, 0x2C, 0x18, 0x30, 0x21, 0x02, 0x05, 0x0B,
       0x17, 0x2E, 0x1C, 0x38, 0x31, 0x23, 0x06, 0x0D, 0x1B, 0x36, 0x2D, 0x1A, 0x34, 0x29,
       0x12, 0x24, 0x08, 0x11, 0x22, 0x04, 0x09, 0x13, 0x26, 0x0c, 0x19, 0x32, 0x25, 0x0a,
       0x15, 0x2a, 0x14, 0x28, 0x10, 0x20};

    unsigned char permutation[16] = {9, 15, 8, 13, 10, 14, 12, 11, 0, 1, 2, 3, 4, 5, 6, 7};

    unsigned char shiftRow[16] = {0, 1, 2, 3, 7, 4, 5, 6, 10, 11, 8, 9, 13, 14, 15, 12};

    unsigned char roundTweakey1[16], roundTweakey2[16], roundTweakey3[16];

    unsigned char roundTweakey1Temp[16], roundTweakey2Temp[16], roundTweakey3Temp[16];

    int numberOfRounds = 56;

    for (int i = 0; i < 16; i++)
    {
        roundTweakey1[i] = k[i];
        roundTweakey2[i] = k[i + 16];
        roundTweakey3[i] = k[i + 32];
    }

    for (int roundNo = 0; roundNo < numberOfRounds; roundNo++)
    {

        // 1 - SubCell Start
        if (roundNo == 0)
        {
            for (int i = 0; i < 16; i++)
            {
                c[i] = sBox8[p[i]];
            }
        }
        else
        {
            for (int i = 0; i < 16; i++)
            {
                c[i] = sBox8[c[i]];
            }
        }

        // SubCell End




        // 2 -AddConstants Start
        c[0] = c[0] ^ (roundConstants[roundNo] & 0x0F);
        c[4] = c[4] ^ (roundConstants[roundNo] >> 4);
        c[8] = c[8] ^ 0x02;
        // AddConstants End






        // 3 - AddRoundTweakey Start
        for (int i = 0; i < 8; i++)
        {
            c[i] = c[i] ^ roundTweakey1[i] ^ roundTweakey2[i] ^ roundTweakey3[i];
        }

        // Perform  Permutation
        for (int i = 0; i < 16; i++)
        {
            roundTweakey1Temp[i] = roundTweakey1[permutation[i]];
            roundTweakey2Temp[i] = roundTweakey2[permutation[i]];
            roundTweakey3Temp[i] = roundTweakey3[permutation[i]];
        }

        memcpy(roundTweakey1, roundTweakey1Temp, sizeof(roundTweakey1));
        memcpy(roundTweakey2, roundTweakey2Temp, sizeof(roundTweakey2));
        memcpy(roundTweakey3, roundTweakey3Temp, sizeof(roundTweakey3));

        // Perform LSFT
        for (int i = 0; i < 8; i++)
        {
            roundTweakey2[i] = ((roundTweakey2[i] << 1) ^ (((roundTweakey2[i] >> 7) ^ (roundTweakey2[i] >> 5)) & 0x01010101U));
            roundTweakey3[i] = ((roundTweakey3[i] >> 1) ^ (((roundTweakey3[i] << 7) ^ (roundTweakey3[i] << 1)) & 0x80808080U));
        }
        // AddRoundTweakey End






        // 4 - ShiftRows Start
        unsigned char cTempForShiftRow[16];
        memcpy(cTempForShiftRow, c, sizeof(cTempForShiftRow));

        for (int i = 0; i < 16; i++)
        {
            c[i] = cTempForShiftRow[shiftRow[i]];
        }
        // ShiftRows End






        // 5 - MixColumn Start
        unsigned char cTempForMixColumn[16];
        memcpy(cTempForMixColumn, c, sizeof(cTempForMixColumn));
        // row 1
        for (int i = 0; i < 4; i++)
        {
            c[i] = cTempForMixColumn[i] ^ cTempForMixColumn[i + 8] ^ cTempForMixColumn[i + 12];
        }

        // row 2
        for (int i = 4; i < 8; i++)
        {
            c[i] = cTempForMixColumn[i - 4];
        }

        // row 3
        for (int i = 8; i < 12; i++)
        {
            c[i] = cTempForMixColumn[i - 4] ^ cTempForMixColumn[i];
        }

        // row 3
        for (int i = 12; i < 16; i++)
        {
            c[i] = cTempForMixColumn[i - 4] ^ cTempForMixColumn[i - 12];
        }
        // MixColumn End
    }
}
